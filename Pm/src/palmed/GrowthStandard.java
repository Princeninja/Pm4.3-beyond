	// add standard data to growth chart
	public static void addBMI( XYModel m, double[] bmi5th, double[] bmi10th, double[] bmi25th, double[] bmi50th, double[] bmi75th, double[] bmi90th, double[] bmi95th ){
		
		double months;
		double years;

		months = 24;
		years = months / 12;
		m.addValue( "5th", years, bmi5th[0] );
		m.addValue( "10th", years, bmi10th[0] );
		m.addValue( "25th", years, bmi25th[0] );
		m.addValue( "50th", years, bmi50th[0] );
		m.addValue( "75th", years, bmi75th[0] );
		m.addValue( "90th", years, bmi90th[0] );
		m.addValue( "95th", years, bmi95th[0] );
		months += .5;
		
		for ( int i = 1; i < bmi5th.length; ++i ){
			years = months / 12;
			m.addValue( "5th", years, bmi5th[i] );
			m.addValue( "10th", years, bmi10th[i] );
			m.addValue( "25th", years, bmi25th[i] );
			m.addValue( "50th", years, bmi50th[i] );
			m.addValue( "75th", years, bmi75th[i] );
			m.addValue( "90th", years, bmi90th[i] );
			m.addValue( "95th", years, bmi95th[i] );
			months += 1;
		}
	}

	
	
	
	
	
	
	
	
